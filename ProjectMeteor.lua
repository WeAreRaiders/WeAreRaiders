local function FJBP_fake_script() -- WrathGui.LocalScript 
	local script = Instance.new('LocalScript', WrathGui)

	local frame = script.Parent
	local centerImageButton = frame:FindFirstChild("Orbit")
	local orbitingImageLabel = frame:FindFirstChild("Orbiter")
	
	-- Vérifier que les ImageLabels ont été trouvés
	if not centerImageButton then
		warn("L'ImageButton 'Orbit' n'a pas été trouvé. Assurez-vous que le nom est correct.")
		return
	end
	
	if not orbitingImageLabel then
		warn("L'ImageLabel 'Orbiter' n'a pas été trouvé. Assurez-vous que le nom est correct.")
		return
	end
	
	-- Paramètres de l'orbite
	local orbitRadius = 45-- Distance entre le centre et l'ImageLabel en orbite
	local orbitSpeed = 1 -- Vitesse de rotation en radians par seconde
	local angle = 0 -- Angle initial
	
	-- Fonction pour mettre à jour la position de l'ImageLabel en orbite
	local function updateOrbit(deltaTime)
		-- Mettre à jour l'angle en fonction du temps écoulé
		angle = angle + orbitSpeed * deltaTime
	
		-- Calculer la nouvelle position en utilisant les coordonnées polaires
		local centerX = centerImageButton.AbsolutePosition.X + (centerImageButton.AbsoluteSize.X / 2)
		local centerY = centerImageButton.AbsolutePosition.Y + (centerImageButton.AbsoluteSize.Y / 2)
		local x = centerX + orbitRadius * math.cos(angle)
		local y = centerY + orbitRadius * math.sin(angle)
	
		-- Mettre à jour la position de l'ImageLabel en orbite
		orbitingImageLabel.Position = UDim2.new(0, x - frame.AbsolutePosition.X - (orbitingImageLabel.Size.X.Offset / 2), 0, y - frame.AbsolutePosition.Y - (orbitingImageLabel.Size.Y.Offset / 2))
	end
	
	-- Connecter la fonction à l'événement RenderStepped pour une
	game:GetService("RunService").RenderStepped:Connect(function(deltaTime)
		updateOrbit(deltaTime)
	end)
	
	
	--Par Empyreus
end
